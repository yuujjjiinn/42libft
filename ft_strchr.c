#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*n;

	n = 0;
	while (*s)
	{
		if (*s == (char) c)
			n = s;
		s++;
	}
	if (c == 0)
		n = s;
	return (n);
}
