#include "libft.h"

size_t	ft_strlen(const char *s)
{
	int	n;
	int	i;

	n = 0;
	i = 0;
	while (s[i] != '\0')
	{
		n++;
		i++;
	}
//	write(1,"k",1);
	return (n);
}
