#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	i;
	int	n;
	int	c;

	i = 0;
	n = 1;
	c = 0;
	while (nptr[i] == ' ' || nptr[i] == '+' || nptr[i] == '-')
	{
		if (nptr[i] == '-')
			n *= -1;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		c *= 10;
		c += nptr[i] - 48;
		i++;
	}
	c *= n;
	return (c);
}
