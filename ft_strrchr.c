#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	n;

	n = ft_strlen(s);
	s += n;
	if (c == 0)
		return (s);
	while (n--)
	{
		if (*s == (char)c)
			return (s);
		s--;
	}
	return (0);
}
